chislo = int(input("Введите число")) #Запрашивает у пользователя число
if chislo % 2 == 0: #первое условие оператора ветвления
    print("Число", chislo, "четное")#выполнение первого условия оператора ветвления
else:
    print("Число", chislo, "нечетное")#выполнение условия иначе оператора ветвления

